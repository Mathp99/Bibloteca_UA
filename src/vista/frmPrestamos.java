    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        if (!txtNombreCliente.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Eliminar el registro?", "Confirmar", 2);

            if (confirmacion == 0) {
                modPrestamo func = new modPrestamo();
                gsPrestamo dts = new gsPrestamo();

                dts.setNombreCliente(txtNombreCliente.getText());
                func.eliminar(dts);
                vaciarCampos();
                mostrar("");
                accion = "guardar";
            }
        }
    }//GEN-LAST:event_btnDevolverActionPerformed

    private void btnPrestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrestarActionPerformed
        gsPrestamo dts = new gsPrestamo();
        modPrestamo func = new modPrestamo();

        int cicliente = Integer.parseInt(txtCICliente.getText());
        dts.setIdCliente(cicliente);

        dts.setCant1(Integer.parseInt(txtCantLibro1.getText()));
        dts.setCant2(Integer.parseInt(txtCantLibro2.getText()));
        dts.setCant3(Integer.parseInt(txtCantLibro3.getText()));

        dts.setNombreCliente(txtNombreCliente.getText());
        dts.setFechaPrest(txtFechaPrest.getText());
        dts.setFechaDevol(txtFechaDevol.getText());

        int seleccionLibro1 = cmbPrestLibro1.getSelectedIndex();
        dts.setLibro1((String) cmbPrestLibro1.getItemAt(seleccionLibro1));
        int seleccionLibro2 = cmbPrestLibro2.getSelectedIndex();
        dts.setLibro2((String) cmbPrestLibro2.getItemAt(seleccionLibro2));
        int seleccionLibro3 = cmbPrestLibro3.getSelectedIndex();
        dts.setLibro3((String) cmbPrestLibro3.getItemAt(seleccionLibro3));

        if (accion.equals("guardar")) {
            if ((func.insertarCli(dts)) && (func.insertarPrest(dts))) {
                JOptionPane.showMessageDialog(rootPane, "Registo exitoso");
                vaciarCampos();
                mostrar("");
            }
        }

    }//GEN-LAST:event_btnPrestarActionPerformed

