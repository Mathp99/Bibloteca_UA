    private void tablaPrestamosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPrestamosMouseClicked
        accion = "editar";
        
        int fila = tablaPrestamos.rowAtPoint(evt.getPoint());
        txtNombreCliente.setText(tablaPrestamos.getValueAt(fila, 0).toString());
        cmbPrestLibro1.setSelectedItem(tablaPrestamos.getValueAt(fila, 1).toString());
        txtCantLibro1.setText(tablaPrestamos.getValueAt(fila, 2).toString());
        cmbPrestLibro2.setSelectedItem(tablaPrestamos.getValueAt(fila, 3).toString());
        txtCantLibro2.setText(tablaPrestamos.getValueAt(fila, 4).toString());
        cmbPrestLibro3.setSelectedItem(tablaPrestamos.getValueAt(fila, 5).toString());
        txtCantLibro3.setText(tablaPrestamos.getValueAt(fila, 6).toString());
        txtFechaPrest.setText(tablaPrestamos.getValueAt(fila, 7).toString());
        txtFechaDevol.setText(tablaPrestamos.getValueAt(fila, 8).toString());
    }//GEN-LAST:event_tablaPrestamosMouseClicked

