    private void vaciarCampos() {
        txtCICliente.setText("");
        txtNombreCliente.setText("");
        txtCantLibro1.setText("");
        txtCantLibro2.setText("");
        txtCantLibro3.setText("");
        txtFechaPrest.setText("");
        txtFechaDevol.setText("");
        cmbPrestLibro1.setSelectedIndex(-1);
        cmbPrestLibro2.setSelectedIndex(-1);
        cmbPrestLibro3.setSelectedIndex(-1);
    }

    private void llenarcmbLibro() {
        try {
            sSQL = "select * from libros";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sSQL);
            while (rs.next()) {
                String libro = rs.getString("tituloLibro");
                cmbPrestLibro1.addItem(libro);
                cmbPrestLibro2.addItem(libro);
                cmbPrestLibro3.addItem(libro);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

