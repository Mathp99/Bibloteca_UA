    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        /*if ((txtISBN.getText().length() == 0) || (txtTitulo.getText().length() == 0) || (cmbAutor.getText().length() == 0) || (cmbGenero.getText().length() == 0) || (cmbEditorial.getText().length() == 0) || (txtStock.getText().length() == 0)) {
            JOptionPane.showMessageDialog(rootPane, "Asegúrate de llenar todos los campos.");
        }*/

        gsLibro dts = new gsLibro();
        modLibro func = new modLibro();

        dts.setIsbn(txtISBN.getText());
        dts.setTitulo(txtTitulo.getText());

        int seleccionAutor = cmbAutor.getSelectedIndex();
        dts.setAutor((String) cmbAutor.getItemAt(seleccionAutor));
        int seleccionGenero = cmbGenero.getSelectedIndex();
        dts.setGenero((String) cmbGenero.getItemAt(seleccionGenero));
        int seleccionEditorial = cmbEditorial.getSelectedIndex();
        dts.setEditorial((String) cmbEditorial.getItemAt(seleccionEditorial));
        dts.setFechaPublic(txtFechaPublic.getText());
        dts.setStock(txtStock.getText());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Registo exitoso");
                vaciarCampos();
                mostrar("");
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

