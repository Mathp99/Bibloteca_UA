            String autor = (String) cmbAutor.getSelectedItem();
            String editorial = (String) cmbEditorial.getSelectedItem();
            String genero = (String) cmbGenero.getSelectedItem();

            sSQL = "update libros set idLibro='" + idlibro + "', tituloLibro='" + titulo + "', autor='" + autor + "',"
                    + "genero='" + genero + "', editorial='" + editorial + "', anhoPublic='" + fecha + "',"
                    + "stock='" + stock + "' where idLibro='" + idlibro + "'";

            PreparedStatement pst = cn.prepareStatement(sSQL);
            pst.execute();

            JOptionPane.showMessageDialog(rootPane, "Registo editado");
            vaciarCampos();
            mostrar("");
            accion = "guardar";
