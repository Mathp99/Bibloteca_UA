    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        gsEditorial dts = new gsEditorial();
        modEditorial func = new modEditorial();

        int ideditorial = Integer.parseInt(txtIDEditorial.getText());
        dts.setIdEditorial(ideditorial);
        dts.setNombreEditorial(txtNombreEditorial.getText());
        dts.setDireccEditorial(txtDireccion.getText());
        int telefeditorial = Integer.parseInt(txtTelefono.getText());
        dts.setTelefEditorial(telefeditorial);

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Registo exitoso");
                vaciarCampos();
                mostrar("");
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

